// C Program for driving license

#include <stdio.h>

int main()
{

    int age;

    printf("Enter your age\n");
    scanf("%d", &age);

    if (age >= 18, age <= 90)
    {

        printf("Congrats you can get a driving license");
    }

    else
    {

        printf("Sorry you are not able to drive car");
    }

    return 0;
}